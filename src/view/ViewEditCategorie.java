    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Categories categories = new Categories();
        //Modifier La categorie
        String libelle = txtLibelle.getText().trim();
        String gender = "";

        if(rbMan.isSelected())
        {
            gender = "Homme";
        }  
        else{
            if(rbUnisex2.isSelected()){
                gender = "Unisex";
            }
            else{
                gender = "Femme";
             }
        }
        
        

     
        //recuperer la categorie

        int id = Integer.parseInt(textID.getText().trim());

        categories.setNameCategorie(libelle);
        categories.setGenre(gender);
        categories.setIdCategories(id);

        DbStock db = new DbStock();
        boolean b = db.updateCategorie(categories);
        if(b){
            //pour afficher a l'ecran
            String result = libelle+ "\n";
            JOptionPane.showMessageDialog(this, "SUCCESS! Categorie " +result+ "modifiée");
            loadAllCategorie();
        }
        else{
            JOptionPane.showMessageDialog(this, "ERROR! categorie non modifiée");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed
