    public void loadAllCategorie(){
        dftm = new DefaultTableModel();
         //indiquer les valeurs de l'entete
         dftm.setColumnIdentifiers(tab);
         
         
         DbStock db = new DbStock();
         db.loadCategorie(dftm, jTCategorie);
    }
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        String id = textID.getText();

        if (id.equals("")==false)
        {
            int idInt =Integer.parseInt(id);
            DbStock db = new DbStock();
            int choice = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer la categorie "+txtLibelle.getText()+" ?", "Message",JOptionPane.YES_NO_OPTION);
            if (choice ==JOptionPane.YES_OPTION)
            {
                boolean b = db.deleteCategorie(idInt);
                // appel rechargement des etudiants
                
                if (b)
                {
                    loadAllCategorie();
                    JOptionPane.showMessageDialog(this, "Categorie "+txtLibelle.getText()+" supprimée");

                    txtLibelle.setText("");

                    rbMan.setSelected(false);
                    rbWoman.setSelected(false);
                    textID.setText("");
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Categorie non supprimée");
                }

            }

        }

    }//GEN-LAST:event_btnDeleteActionPerformed

