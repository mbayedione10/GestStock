    private void jTCategorieMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTCategorieMouseClicked
        // TODO add your handling code here:
         int ligne =jTCategorie.getSelectedRow();
        String id = jTCategorie.getValueAt(ligne, 0).toString();
        textID.setText(id);
        String libelle = jTCategorie.getValueAt(ligne, 1).toString();
        txtLibelle.setText(libelle);
        String genre = jTCategorie.getValueAt(ligne, 2).toString();
             if (genre.startsWith("H"))
        {
            rbMan.setSelected(true);
            
        }
        else
        {
            if(genre.startsWith("U"))
            {
                rbUnisex2.setSelected(true);
            }
            else{
                 rbWoman.setSelected(true);
            }
 
           
        }
        

       
    }//GEN-LAST:event_jTCategorieMouseClicked

