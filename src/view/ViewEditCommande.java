    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Commandes commande = new Commandes();
        DbStock db = new DbStock();

        //Modifier La categorie
        String libelle = txtLibelle.getText().trim();
        //String designation = txtDesignation.getText().trim();

        String article =cArticle.getSelectedItem().toString();
        String client = cClient.getSelectedItem().toString();
        int qte = Integer.parseInt(txtQuantite.getText().trim());
        
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        System.out.println(dateFormat.format(date));
        

        int  idArticle ;
        int idClient;

        //recuperer l'article

        int id = Integer.parseInt(textID.getText().trim());
        
       
        //enregistrer idArticle dans le model article
        idArticle = db.idArticle(article);
        commande.setIdArticle(idArticle);
        commande.setDate(dateFormat.format(date));
        idClient = db.idClient(client);
        commande.setLibelle(libelle);
        commande.setIdClient(idClient);
        int idUser = db.idLogin(ViewLogin.loginUser);
        commande.setIdUser(idUser);
        commande.setQte(qte);
        commande.setIdCommande(id);


        boolean b = db.updateCommande(commande);

        if(b){

                //pour afficher a l'ecran
                JOptionPane.showMessageDialog(this, "SUCCESS! commande " +libelle+ " du client " +client+ " modifiée");

            //reinitialiser les donnees
            loadAllCommande();

            txtLibelle.setText("");
            textID.setText("");
            
            txtQuantite.setText("");
           
        }
        else{
            JOptionPane.showMessageDialog(this, "ERROR! Commande  non modifié");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        String id = textID.getText();

        if (id.equals("")==false)
        {
            int idInt =Integer.parseInt(id);
            DbStock db = new DbStock();
            int choice = JOptionPane.showConfirmDialog(this, "Voulez vous vraiment supprimer la commande "+txtLibelle.getText()+" ?", "Message",JOptionPane.YES_NO_OPTION);
            if (choice ==JOptionPane.YES_OPTION)
            {
                boolean b = db.deleteCommande(idInt);
                // appel rechargement des articles

                if (b)
                {
                    loadAllCommande();
                    JOptionPane.showMessageDialog(this, "Commande "+txtLibelle.getText()+" supprimée");

                    txtLibelle.setText("");
                    textID.setText("");
                    txtQuantite.setText("");
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Commande non supprimé");
                }

            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

