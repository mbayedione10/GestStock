    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        DbStock db = new DbStock();
          int idClient,idArticle;
        int idCommande, qteCommande, prixU;
        String libelle = txtLibelle.getText().trim();
        String commande = cCommande.getSelectedItem().toString();
        //String client = cClient.getSelectedItem().toString();
        //recuperer l'id de la commande
        idCommande = db.idCommande(commande);
        //recuperer l√≠d de l'article
        idArticle = db.idArticleCommande(commande);
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        System.out.println(dateFormat.format(date));
        
        prixU = db.PrixArticle(idArticle);
        String nomArticle = db.nomArticle(idArticle);
        qteCommande = db.qteCommandeNom(commande);
        String nomClient = db.nomClientCommande(commande);
       
        int montantFact = qteCommande*prixU;
         //recuperer la commande
        Facture fact = new Facture();
        fact.setLibelleFac(libelle);
        fact.setDateFac(dateFormat.format(date));
        //enregistrer id dans le la facture
        idCommande = db.idCommande(commande);
        fact.setIdCommande(idCommande);
        fact.setQte(qteCommande);
        fact.setNomClient(nomClient);
        int idUser = db.idLogin(ViewLogin.loginUser);
        fact.setIdUser(idUser);
        fact.setMontantFac(montantFact);
        fact.setNomArticle(nomArticle);
        int idStock =db.idStockArticle(nomArticle);
            int qteStock = db.qteStock(idStock) - qteCommande;
            int qteArticle = qteStock;
        if(qteCommande<=db.qteStock(idStock)){
                int choice = JOptionPane.showConfirmDialog(this, "Voulez vous enregistrer cette facture?", "Message",JOptionPane.YES_NO_OPTION);
                if (choice ==JOptionPane.YES_OPTION) {
                boolean b = db.saveFacture(fact);

            if(b){
                Articles article = new Articles();

                int prix = qteArticle*prixU;

                article.setQte(qteArticle);
                article.setPrixT(prix);
                article.setIdArticle(idArticle);

                db.updateArticleQte(article);
                 //enregistre entree stock dans la table stock

                //appel de la table stock, ajouter la quantite

                Stock stock = new Stock();
                stock.setNomArticle(nomArticle);
                stock.setMontantStock(prix);

                stock.setQteStock(qteStock);
                boolean a = db.saveStock(stock);

                EtatStock etatstock = new EtatStock();
                int idEstock = db.idEStockArticle(nomArticle);

                etatstock.setIdEStock(idEstock);
                etatstock.setMontantEStock(prix);
                etatstock.setNomEArticle(nomArticle);
                etatstock.setQteEStock(qteStock);
                boolean c = db.updateEStock(etatstock);


               if(a && c){
                  db.deleteCommandeNom(commande);
                //pour afficher a l'ecran
                JOptionPane.showMessageDialog(this, "SUCCESS! facture  " +libelle+ " de " +montantFact+ " enregistr√©e");
                //reinitialiser les donnees

                txtLibelle.setText("");
                }       
                }
        }
        }
        else{
            JOptionPane.showMessageDialog(this, "ERROR! facture  non enregistr√©e. Verifiez si la quantit√© de la commande est disponible dans le stock!!");
        
        }
    }//GEN-LAST:event_btnSaveActionPerformed

